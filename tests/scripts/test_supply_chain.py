def test_python_lockfiles_are_hash_complete() -> None:
    for path in LOCK_FILES:
        text = path.read_text(encoding="utf-8")
        assert "autogenerated by uv" in text
        assert "git+" not in text
        blocks = _requirement_blocks(text)
        assert blocks, f"{path} contains no locked requirements"
        for block in blocks:
            first_line = block.splitlines()[0]
            assert "==" in first_line or " @ https://" in first_line, (
                f"{path}: mutable requirement {first_line!r}"
            )
            assert "--hash=sha256:" in block, (
                f"{path}: requirement lacks a SHA-256 hash: {first_line!r}"
            )


def test_workflow_pip_installs_are_locked_or_dependency_free() -> None:
    violations: list[str] = []

    for path, workflow in _workflows():
        for step in _workflow_steps(workflow):
            for line in str(step.get("run", "")).splitlines():
                command = line.strip()
                if "pip install" not in command:
                    continue
                if "--require-hashes" in command:
                    continue
                if "--no-deps" in command and " -e " in f" {command} ":
                    continue
                violations.append(f"{path.name}: {command}")

    assert not violations, (
        f"workflow pip installs must use reviewed locks: {violations}"
    )


def test_fabric_provider_is_exact_and_multi_platform_locked() -> None:
    terraform_root = REPO_ROOT / "deploy" / "terraform"
    providers = (terraform_root / "providers.tf").read_text(encoding="utf-8")
    lock = (terraform_root / ".terraform.lock.hcl").read_text(encoding="utf-8")

    version_match = re.search(
        r'source\s*=\s*"microsoft/fabric"\s+version\s*=\s*"([^"]+)"',
        providers,
    )
    assert version_match
    version = version_match.group(1)
    assert re.fullmatch(r"\d+\.\d+\.\d+", version)
    assert f'version     = "{version}"' in lock
    assert f'constraints = "{version}"' in lock
    assert lock.count('"h1:') >= 6
    assert lock.count('"zh:') >= 6


def test_notebook_runtime_installs_are_exactly_pinned() -> None:
    for path in NOTEBOOK_TEMPLATES:
        tree = ast.parse(path.read_text(encoding="utf-8"))
        pip_requirements: list[str] = []
        for call in (node for node in ast.walk(tree) if isinstance(node, ast.Call)):
            if (
                not isinstance(call.func, ast.Attribute)
                or call.func.attr != "check_call"
            ):
                continue
            if not call.args or not isinstance(call.args[0], ast.List):
                continue
            values = [
                item.value
                for item in call.args[0].elts
                if isinstance(item, ast.Constant) and isinstance(item.value, str)
            ]
            if "pip" in values and "install" in values:
                install_index = values.index("install")
                requirements = [
                    value
                    for value in values[install_index + 1 :]
                    if not value.startswith("-")
                ]
                pip_requirements.extend(requirements)

        assert pip_requirements, f"{path.name}: expected pinned runtime dependencies"
        assert all(
            re.fullmatch(r"[A-Za-z0-9_-]+==[0-9][A-Za-z0-9._-]*", requirement)
            for requirement in pip_requirements
        )


def test_miniforge_bootstraps_are_pinned_and_verified() -> None:
    shell = (REPO_ROOT / "scripts" / "setup.sh").read_text(encoding="utf-8")
    powershell = (REPO_ROOT / "scripts" / "setup.ps1").read_text(encoding="utf-8")

    assert 'MiniforgeVersion="26.3.2-3"' in shell
    assert "releases/latest" not in shell
    assert shell.count("sha256") >= 5
    assert shell.index('verify_sha256 "$installer"') < shell.index('bash "$installer"')
    assert "$MiniforgeVersion = '26.3.2-3'" in powershell
    assert "--version $MiniforgeVersion --source winget" in powershell
