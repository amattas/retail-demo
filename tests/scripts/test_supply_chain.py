from collections.abc import Iterator
from pathlib import Path

import yaml

REPO_ROOT = Path(__file__).resolve().parents[2]
WORKFLOWS = REPO_ROOT / ".github" / "workflows"
FULL_COMMIT_SHA = re.compile(r"^[0-9a-f]{40}$")
LOCK_FILES = (
    REPO_ROOT / "requirements-docs.txt",
    REPO_ROOT / "requirements-test.txt",
    REPO_ROOT / "utility" / "requirements-ci.txt",
    REPO_ROOT / "utility" / "requirements-deploy.txt",
)
NOTEBOOK_TEMPLATES = (
    REPO_ROOT / "utility" / "notebooks" / "templates" / "driver-02-dimensions.py",
    REPO_ROOT / "utility" / "notebooks" / "templates" / "driver-03-facts.py",
    REPO_ROOT / "utility" / "notebooks" / "templates" / "driver-04-gold.py",
)


def _workflow_steps(workflow: dict) -> Iterator[dict]:
    for job in workflow.get("jobs", {}).values():
        yield from job.get("steps", [])


def _workflows() -> Iterator[tuple[Path, dict]]:
    for path in sorted((*WORKFLOWS.glob("*.yml"), *WORKFLOWS.glob("*.yaml"))):
        yield path, yaml.safe_load(path.read_text(encoding="utf-8"))


def _requirement_blocks(text: str) -> list[str]:
    blocks: list[list[str]] = []
    current: list[str] = []
    for line in text.splitlines():
        if line and not line[0].isspace() and not line.startswith("#"):
            if current:
                blocks.append(current)
            current = [line]
        elif current:
            current.append(line)
    if current:
        blocks.append(current)
    return ["\n".join(block) for block in blocks]


def test_plugin_marketplaces_use_sha_pinned_local_checkouts() -> None:
    for path, workflow in _workflows():
        checkout_paths: dict[str, tuple[str, str]] = {}
        steps = list(_workflow_steps(workflow))
        for step in steps:
            uses = str(step.get("uses", ""))
            options = step.get("with", {})
            if not uses.startswith("actions/checkout@") or "repository" not in options:
                continue
            checkout_path = str(options.get("path", "")).removeprefix("./").rstrip("/")
            checkout_paths[checkout_path] = (
                str(options["repository"]),
                str(options.get("ref", "")),
            )

        for step in steps:
            marketplaces = step.get("with", {}).get("plugin_marketplaces")
            if not marketplaces:
                continue
            for marketplace in str(marketplaces).splitlines():
                marketplace = marketplace.strip()
                assert marketplace.startswith("./"), (
                    f"{path.name}: plugin marketplace must be a local pinned checkout: "
                    f"{marketplace!r}"
                )
                checkout_path = marketplace.removeprefix("./").rstrip("/")
                assert checkout_path in checkout_paths, (
                    f"{path.name}: no external checkout supplies {marketplace!r}"
                )
                repository, ref = checkout_paths[checkout_path]
                assert repository and FULL_COMMIT_SHA.fullmatch(ref), (
                    f"{path.name}: {repository} marketplace ref must be a full SHA: "
                    f"{ref!r}"
                )


def test_checkout_actions_never_persist_credentials() -> None:
    violations: list[str] = []

    for path, workflow in _workflows():
        for step in _workflow_steps(workflow):
            if not str(step.get("uses", "")).startswith("actions/checkout@"):
                continue
            if step.get("with", {}).get("persist-credentials") is not False:
                violations.append(f"{path.name}: {step.get('name', 'checkout')}")

    assert not violations, f"checkout credentials must not persist: {violations}"


